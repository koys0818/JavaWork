package com.lec.java.for04;

public class For04Main {

	public static void main(String[] args) {
		System.out.println("for문 연습");
		int i;
		for(i=1;i<100;i++)
		{
			if(i % 14 == 0)
			{
				System.out.println("i = "+ i);
			}
		}
		int a = 0;
		for(i=0;i<=10;i++)
		{
			a = a + i;
			System.out.println("total = " + a);
			
		}
		
	} // end main ()

} // end class For04Main










