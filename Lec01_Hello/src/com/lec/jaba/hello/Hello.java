package com.lec.jaba.hello;

public class Hello {

	public static void main(String[] args) {
		System.out.println("Hello Java"); //한줄 주석 
		System.out.println("안녕하세요");	//println() 은 화면 출력후 한줄띄기
	
		System.out.println();
	
		System.out.println(1+2);
		System.out.println("1"+"2");
		System.out.println('A'+'B');
		System.out.println('1'+2);
		System.out.println();
		System.out.println('J' + "ava");
		/*
 * Java 첫 프로그램.
 * [학습목표]
 * - 기본 출력 : println(), print() 
 * - 주석 (Comment)
 */
		System.out.print("자바");
		
		System.out.print("프레임워크");
		// print()는 출력하고 줄 안 바꿈 
		System.out.println("풀스텍과정");
		
		System.out.println("20200316");
	}

}
